
""" Implementar un bucle for para iterar sobre una lista de números e imprimir sus 
cuadrados. """
#lista de números
numeros = [1, 2, 3, 4, 5]

# Iterar sobre la lista usando un bucle for
for numero in numeros:
    # Calcular el cuadrado del número
    cuadrado = numero ** 2
    # Imprimir el número y su cuadrado
    print(f"El cuadrado de {numero} es {cuadrado}")
